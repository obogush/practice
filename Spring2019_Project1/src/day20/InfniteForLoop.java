package day20;

public class InfniteForLoop {

  public static void main(String[] args) {
    
    for (int i = 0; i <0 ; i++) {
      System.out.println("i " + i);
    }
    
    System.out.println("end");
    
    for (int i = 0; i <10 ; i--) {
      System.out.println("i " + i);
    }
    
    System.out.println("The end");
    
    
    

  }

}


