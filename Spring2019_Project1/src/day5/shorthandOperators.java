
package day5;

public class shorthandOperators {


	public static void main(String[] args) {
		
		int i = 15;
		System.out.println("the score is: " + i);
		
		 i = i + 10; // i = 15;
		 
		 
		 
		 
		 System.out.println("the score is: " + i);
		 
		 i = i - 3;
		 
		 System.out.println("the score is: " + i);
		 
		 i = i * 13;
		 
		 System.out.println("the score is: " + i);
		 i = i / 10;
		 System.out.println("the score is: " + i);
		 i = i % 15;
		 System.out.println("the score is: " + i);
		 
		 
		 
	}


	}
